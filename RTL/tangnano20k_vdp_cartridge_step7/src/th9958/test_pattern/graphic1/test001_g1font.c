#include <stdio.h>
#include <stdlib.h>
#include "v9968.h"

// --------------------------------------------------------------------
int main( int argc, char *argv[] ) {
	int i;

	v9968_set_screen1();

	v9968_set_write_vram_address( 0x1800, 0 );
	for( i = 0; i < 32 * 24; i++ ) {
		v9968_write_vram( i );
	}

	v9968_write_vdp( 5, 0x36 );

	v9968_wait_key();
	v9968_exit();
	return 0;
}
